-- Recipes Global Library
CREATE TABLE IF NOT EXISTS public.recipes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    description TEXT,
    ingredients TEXT,
    instructions TEXT,
    calories INTEGER DEFAULT 0,
    protein_g INTEGER DEFAULT 0,
    carbs_g INTEGER DEFAULT 0,
    fats_g INTEGER DEFAULT 0,
    time_mins INTEGER DEFAULT 15,
    category TEXT, -- e.g. 'Breakfast', 'Lunch', etc.
    image_url TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Master Diet Templates
CREATE TABLE IF NOT EXISTS public.diet_templates (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    diet_code TEXT NOT NULL UNIQUE,
    diet_name TEXT NOT NULL,
